    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String titulo = JOptionPane.showInputDialog("Título do livro:");
        String autor = JOptionPane.showInputDialog("Autor do livro:");

        if (titulo != null && autor != null) {
            try {
                book_dao.cadastrar(new LIVRO(titulo, autor));
            } catch (SQLException ex) {
                Logger.getLogger(PAINEL_INICIAL.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Livro cadastrado com sucesso!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void LISTAR_USUARIOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LISTAR_USUARIOActionPerformed
        StringBuilder sb = new StringBuilder();
        try {
            for (CADASTRO_USUARIO Usuario : User_dao.listar()) {
                sb.append(Usuario.toString()).append("\n");
            }
        } catch (SQLException ex) {
            Logger.getLogger(PAINEL_INICIAL.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, sb.toString(), "Usuários", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_LISTAR_USUARIOActionPerformed

