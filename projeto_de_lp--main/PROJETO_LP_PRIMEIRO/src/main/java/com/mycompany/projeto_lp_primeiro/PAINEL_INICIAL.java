    private void cadastrar_usuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrar_usuarioActionPerformed
        String nome = JOptionPane.showInputDialog("Nome do usuário:");
        String email = JOptionPane.showInputDialog("Email do usuário:");
        UsuarioDAO User_dao = new UsuarioDAO();
        if (nome != null && email != null) {
            User_dao(new CADASTRO_USUARIO(nome, email));
            JOptionPane.showMessageDialog(null, "Usuário cadastrado com sucesso!");
        }
    }//GEN-LAST:event_cadastrar_usuarioActionPerformed

